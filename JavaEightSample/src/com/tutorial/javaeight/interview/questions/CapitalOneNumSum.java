	private static void findMinAnkit(List<Integer> num, int k) {

		// reverse to descending order
		num.sort(Comparator.reverseOrder());

		// counter of the current number being
		int ctr = 0;

		for (int i = 0; i < k; i++) {
			// compute the new value & set it back at the same position.
			int newValue = (int) Math.ceil((float) num.get(ctr) / 2);
			num.set(ctr, newValue);
			// reset the ctr to 0 when the last item is not max element.
			if (ctr == num.size() - 1) {
				if (newValue < num.get(0)) {
					ctr = 0;
				}
				continue;
			}
			// keep dividing num until its greater than the next element
			if (newValue > num.get(ctr + 1)) {
				continue;
			} else {
				if (num.get(0) >= num.get(ctr + 1)) {
					num.sort(Comparator.reverseOrder());
					ctr = 0;
				} else {
					ctr++;
				}

			}
		}
		// }

		System.out.println(num.stream().reduce(0, Integer::sum));

	}
